class ResultListCat {
  String? name;
  int? id;
  List<SubCategory>? subCategories;
  String? selectedSubCategory; // Add this field

  ResultListCat({
    this.name,
    this.id,
    this.subCategories,
    this.selectedSubCategory,
  });

  ResultListCat.fromJson(Map<String, dynamic> json) {
    name = json['name'];
    id = json['id'];
    if (json['list'] != null) {
      subCategories = <SubCategory>[];
      json['list'].forEach((v) {
        subCategories!.add(SubCategory.fromJson(v));
      });
    }
    // Initialize selectedSubCategory if needed
    selectedSubCategory = subCategories?.first.name; // Or a default value
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['name'] = name;
    data['id'] = id;
    if (subCategories != null) {
      data['list'] = subCategories!.map((v) => v.toJson()).toList();
    }
    data['selectedSubCategory'] = selectedSubCategory;
    return data;
  }
}

class SubCategory {
  String? name;
  int? id;

