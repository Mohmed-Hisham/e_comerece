class Autogenerated {
  Result? result;

  Autogenerated({this.result});

  Autogenerated.fromJson(Map<String, dynamic> json) {
    result = json['result'] != null ? Result.fromJson(json['result']) : null;
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    if (result != null) {
      data['result'] = result!.toJson();
    }
    return data;
  }
}

class Result {
  Status? status;
  List<ResultListCat>? resultListCat;

  Result({this.status, this.resultListCat});

  Result.fromJson(Map<String, dynamic> json) {
    status = json['status'] != null ? Status.fromJson(json['status']) : null;
    if (json['resultList'] != null) {
      resultListCat = <ResultListCat>[];
      json['resultList'].forEach((v) {
        resultListCat!.add(ResultListCat.fromJson(v));
      });
    }
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    if (status != null) {
      data['status'] = status!.toJson();
    }
    if (resultListCat != null) {
      data['resultList'] = resultListCat!.map((v) => v.toJson()).toList();
    }
    return data;
  }
}

